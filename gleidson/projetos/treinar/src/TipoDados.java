
public class TipoDados {
	
	public static void main(String[] args) {
		byte variavelByte = 127;
		System.out.println(variavelByte);
		short variavelShort = 127;
		System.out.println(variavelShort);
		int variavelInt = 127;
		System.out.println(variavelInt);
		long variavelLong = 126546512655L;
		System.out.println(variavelLong);
		float preco = (float) 0.9999;
		System.out.println(preco);
		double valor = 0.99996546546;
		System.out.println(valor);
		boolean sim = true;
		System.out.println(sim);
		char a = 'A';
		System.out.println("char a = " + a);
		char literal = '\'';// representa o caracter '
		System.out.println("char literal = " + literal);
		
	}
	
}
