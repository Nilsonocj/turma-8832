
public class Incremento {

	public static void main(String[] args) {
//		int soma = 1 + 2;
//		System.out.println(soma);
		
		int i = 0;
		System.out.println(i);
		
		i = i + 1;
		System.out.println(i);
		
		i++;
		
		System.out.println(i);
		
		int incrementeo = ++i;
		
		System.out.println(i);
		System.out.println(incrementeo);
		
		System.out.println(i);
		
	}
	
}
