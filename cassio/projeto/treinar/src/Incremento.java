
public class Incremento {
	
	public static void main(String[] args) {
		//int soma = 1 + 2;
		//System.out.println(soma);
		
		int i = 0;
		System.out.println(i);
		
		i = i + 1;
		System.out.println(i);
		
		i++;//pós incremento atribui primeiro e incrementa depois
		
		System.out.println(i);
		
		int incremento = ++i;//pré incremento incrementa primeiro e atribui depois
		
		System.out.println(i);
		System.out.println(incremento);
		
		
	}
}
