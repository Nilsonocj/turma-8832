
public class Operadores {
	public static void main(String[] args) {
		int ano = 2016;
		int nascimento = 1984;
		System.out.println(ano-nascimento);
		
		int somaAB = ano - nascimento;
		System.out.println(somaAB);
		
		int multiplicaAB = ano * nascimento;
		System.out.println(multiplicaAB);
		
		int divideAB = ano / nascimento;
		System.out.println(divideAB);
		
		double divideABDouble = ano / (double)nascimento;
		System.out.println(divideABDouble);
	
		int moduloAB = ano % nascimento;
		System.out.println(moduloAB);
		
	}

}
