
public class TipoDados {
	
	public static void main (String [] args) {
		byte 	qtdPortas = 5;
		System.out.println (qtdPortas);
		short 	velocMax = 299;
		System.out.println (velocMax);
		int 	saldo = 10325123;
		System.out.println (saldo);
		long	cpf = 1234545654666L; //No caso de Long tem que ter o L no final da variavel
		System.out.println (cpf);
		float	preco = 0.99F; // Poderia ser também --  float	preco = (float) 0.99;
		System.out.println (preco);
		double	valor = 0.9999999; 
		System.out.println (valor);
		boolean	sim = true; 
		System.out.println (sim);
		char	a = 'A'; 
		System.out.println ("char a = " + a);
		
		System.out.println ("Francisco \""); // O caracter \ "scape" funciona para imprimir caracter que serve como delimitador
		

		
		
	}
}
