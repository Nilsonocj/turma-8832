
public class OperadorRelacional {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		boolean aIgualb = a == b;
		System.out.println(aIgualb);
		boolean aDiferenteb = a != b;
		System.out.println(aDiferenteb);
		boolean aMaiorb = a > b;
		System.out.println(aMaiorb);
		boolean aMenorb = a < b;
		System.out.println(aMenorb);
		boolean aMaiorOuIgualb = a >= b;
		System.out.println(aMaiorOuIgualb);
		boolean aMenorOuIgual = a <= b;
		System.out.println(aMenorOuIgual);
	}

}
